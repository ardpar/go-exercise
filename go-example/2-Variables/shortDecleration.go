package main

import (
	"fmt"
)

func main(){
	//Short declaration operator
	x := 4
	fmt.Println(x)
	x = 5
	fmt.Println(x)
	y := 5 + 9
	fmt.Println(y)
	y = y + 20 //when you declare and you must use it
	fmt.Println(y)
	//var keyword
	var z = "example"
	fmt.Println(z)
	var a = 0.2
	fmt.Println(a)

}


